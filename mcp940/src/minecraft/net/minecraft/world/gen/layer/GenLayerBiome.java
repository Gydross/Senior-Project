    /**
     * Returns a list of integer values generated by this layer. These may be interpreted as temperatures, rainfall
     * amounts, or biomeList[] indices based on the particular GenLayer subclass.
     * @param areaX: The region x
     * @param areaY: The region y (z)
     * @param areaWidth: Region width
     * @param areaHeight: Region height
     * 
     * @note Commented out old method for the sake of keeping a backup; original is below this one
     * @author Aaron Wink
     */
    
    public int[] getInts(int areaX, int areaY, int areaWidth, int areaHeight)
    {
        int[] aint = this.parent.getInts(areaX, areaY, areaWidth, areaHeight);
        int[] aint1 = IntCache.getIntCache(areaWidth * areaHeight);  // Chunk's biome array (256 blocks, 16bx16b)

        
    	// Current climate zone; determines assuming zones are 3200 blocks high
    	// Finds the absolute value of the current z coordinate / 3200, then subtracts
        //   some multiple of 8 so it fits within the array.
        int chunkVal = Math.abs(((areaY + 1) << 4) * 16);  // Reverses the bitwise ops to get the z coord, then mults by 16
        int offset = (int) Math.floor(chunkVal / 25600);  // Finds how displaced the current climate band is from the 0th band
        int bandNum = (int) Math.floor(chunkVal / climateZoneHeight);  // Which climate band is this?
        int trueOffset = offset * 8;  // Used to reduce the bandNum to an appropriate number from 0 to 7
    	int zone = bandNum - trueOffset;
    	
        for (int i = 0; i < areaHeight; ++i)
        {
            for (int j = 0; j < areaWidth; ++j)
            {
            	this.initChunkSeed((long)(j + areaX), (long)(i + areaY));  // Initializes the seed for the current chunk
            	int v = j + i * areaWidth;  // Does something, whatever. It's not important
            	
            	if (aint[v] == 0)
            	{
            		aint1[v] = 0;
            	} else {
            		int biomeID = 0;
            		switch (zone)  // Switch-case statements below will require tweaking in near future
            		{
            			case 3:  // Chooses a random biome from the polar climate zone
            			case 4:
            				biomeID = Biome.getIdForBiome(polarBiomes[(int) Math.floor(Math.random() * 6)]); 
        					break;
            			case 2:  // Chooses a random biome from the temperate climate zone
            			case 5:
            				biomeID = Biome.getIdForBiome(temperateBiomes[(int) Math.floor(Math.random() * 7)]); 
            				break;
            			case 1:  // Chooses a random biome from the tropical climate zone
            			case 6:
            				biomeID = Biome.getIdForBiome(tropicalBiomes[(int) Math.floor(Math.random() * 5)]); 
            				break;
            			case 0:  // Chooses a random biome from the subtropical climate zone
            			case 7:
            				biomeID = Biome.getIdForBiome(subtropicalBiomes[(int) Math.floor(Math.random() * 8)]); 
            				break;
            		}
            		aint1[v] = biomeID;  // Assigns biome ID to chunk
            	}
            }
        }
        return aint1;
    }
    
